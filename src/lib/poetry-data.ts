// 获取指定年级的古诗词
export const getPoetryByGrade = (grade: number): Poetry[] => {
  return allPoetry.filter(poetry => poetry.level === grade);
};

// 搜索古诗词
export const searchPoetry = (query: string): Poetry[] => {
  const lowerQuery = query.toLowerCase();
  return allPoetry.filter(poetry => 
    poetry.title.toLowerCase().includes(lowerQuery) ||
    poetry.author.toLowerCase().includes(lowerQuery) ||
    poetry.content.some(line => line.toLowerCase().includes(lowerQuery)) ||
    poetry.keywords.some(keyword => keyword.toLowerCase().includes(lowerQuery))
  );
};
